#include "minishell.h"

int	cd_is_home(char **args)
{
	if (!args[1])
		return (1);
	if (args[1][0] == '~')
		return (1);
	if (args[1][0] == '-' && args[1][1] == '-' && args[1][2] == '\0')
		return (1);
	return (0);
}
